package Chapter2;

public class LogicalOperators {

    /**

     x & y
     (AND)

     x = true
     y = true
     result = true

     x = true
     y = false
     result = false

     x = false
     y = true
     result = false

     x = false
     y = false
     result = false

     **/

    /**

     x | y
     (INCLUSIVE OR)

     x = true
     y = true
     result = true

     x = true
     y = false
     result = true

     x = false
     y = true
     result = true

     x = false
     y = false
     result = false

     **/


    /**

     x ^ y
     (EXCLUSIVE OR)

     x = true
     y = true
     result = false

     x = true
     y = false
     result = true

     x = false
     y = true
     result = true

     x = false
     y = false
     result = false

     **/
}
